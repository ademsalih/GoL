    double canvasWidth;

    double xCounter;
    double yCounter;

    double cellWidth;
    double cellHeight;

    private int x;
    private int y;

    GraphicsContext gc;

    public Board (Canvas canvas) {
        this.canvasWidth = canvas.getWidth();
        this.xCounter = 0.0;
        this.yCounter = 0.0;
        this.cellHeight = Math.floor(canvasWidth / board.length);
        this.cellWidth = Math.floor(canvasWidth / board.length);
        this.gc = canvas.getGraphicsContext2D();
    }

    public void setBoard(byte[][] board) {
        this.board = board;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public byte[][] getBoard() {
        return board;
    }

    public void setCellColor() {

    }


    public void drawGrid() {

        for (int a = 0; a < board.length; a++) {

            for (int b = 0; b < board.length; b++) {
                gc.setStroke(Color.RED);
                gc.strokeRect(xCounter,yCounter,cellWidth,cellHeight);
                xCounter += cellWidth;

                if ((xCounter % (cellWidth*board.length) == 0.0) && (xCounter != 0.0)) {
                    xCounter = 0.0;
                    yCounter += cellHeight;
                }

            }

        }
    }

    public void drawBoard() {

        for (int a = 0; a < board.length; a++) {

            for (int b = 0; b < board.length; b++) {

                if (board[a][b] == 1) {
                    gc.setFill(Color.BLACK);
                    gc.fillRect(xCounter, yCounter, cellWidth, cellHeight);
                    xCounter += cellWidth;

                } else {
                    gc.setFill(Color.WHITE);
                    gc.fillRect(xCounter,yCounter,cellWidth,cellHeight);
                    xCounter += cellWidth;

                }
                if ((xCounter % (cellWidth*board.length) == 0.0) && (xCounter != 0.0)) {

                    xCounter = 0.0;
                    yCounter += cellHeight;

                }

            }

        }
    }

    public void drawBoardWithGrid() {

        for (int a = 0; a < board.length; a++) {

            for (int b = 0; b < board.length; b++) {

                if (board[a][b] == 1) {
                    //gc.setFill(Color.rgb(110,110,110));
                    gc.setFill(Color.rgb(20,120,230));
                    gc.fillRect(xCounter, yCounter, cellWidth, cellHeight);

                    gc.setLineWidth(3.0);
                    gc.setStroke(Color.rgb(5,5,5));
                    gc.strokeRect(xCounter,yCounter,cellWidth,cellHeight);

                    xCounter += cellWidth;

                } else {
                    gc.setFill(Color.rgb(25,25,25));
                    gc.fillRect(xCounter,yCounter,cellWidth,cellHeight);

                    gc.setLineWidth(3.0);
                    gc.setStroke(Color.rgb(5,5,5));
                    gc.strokeRect(xCounter,yCounter,cellWidth,cellHeight);

                    xCounter += cellWidth;
                }

                if ((xCounter % (cellWidth*board.length) == 0.0) && (xCounter != 0.0)) {
                    xCounter = 0.0;
                    yCounter += cellHeight;
                }

            }

        }

        yCounter = 0.0;
    }

    public void clearBoard() {
        board = new byte[board.length][board.length];
        drawBoardWithGrid();
    }



